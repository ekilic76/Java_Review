package week01_review;

public class EscapeSequences {

    public static void main(String[] args) {

        // \t horizontal tab
        System.out.println("\tHello Batch 28 \tStudents");

        // \n new line
        System.out.println("Hello Students \n\tHow are you all today?");

        // \" double quote
        System.out.println("Hello \"CYDEO\" Batch 28 students");

        // \\ backslash
        System.out.println("      ^");
        System.out.println("     / \\");
        System.out.println("    /   \\");
        System.out.println("    _____");


    }

}
